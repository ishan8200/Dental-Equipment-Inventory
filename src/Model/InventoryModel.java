package Model;

/**
 *
 * @author Ishan Maharjan
 */
public class InventoryModel {
    String productID, productName, productCompany, addedDate;
    int productQuantity;
    double productPrice;

    public InventoryModel(String productID, String productName, String productCompany, double productPrice, int productQuantity, String addedDate) {
        this.productID = productID;
        this.productName = productName;
        this.productCompany = productCompany;
        this.productPrice = productPrice;
        this.productQuantity = productQuantity;
        this.addedDate = addedDate;
    }

    public String getProductID() {
        return productID;
    }

    public void setProductID(String productID) {
        this.productID = productID;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductCompany() {
        return productCompany;
    }

    public void setProductCompany(String productCompany) {
        this.productCompany = productCompany;
    }

    public double getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(double productPrice) {
        this.productPrice = productPrice;
    }

    public int getProductQuantity() {
        return productQuantity;
    }

    public void setProductQuantity(int productQuantity) {
        this.productQuantity = productQuantity;
    }

    public String getAddedDate() {
        return addedDate;
    }

    public void setAddedDate(String addedDate) {
        this.addedDate = addedDate;
    }
}
